class Solution:
    def chalkReplacer(self, chalk: [int], k: int) -> int:
        n = len(chalk)
        pre_sum = [0]*(n)
        pre_sum[0] = chalk[0]

        for i in range(1,n):
            pre_sum[i] = pre_sum[i-1]+chalk[i]
        
        k %= pre_sum[-1]
        l = 0
        r = n-1
        ans = 0
        while l<=r:
            m = (l+r)//2
            if pre_sum[m]>k:
                ans = m
                r = m-1
            else:
                l = m+1
        return ans
